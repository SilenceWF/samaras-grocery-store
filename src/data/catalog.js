export function getProductsByTab(tabId) {
  return products.filter((product) => product.tabs.includes(tabId))
}

export function getProductById(productId) {
  return products.find((product) => product.id === productId)
}

export function getProductsByCategory(categoryId) {
  return products.filter((product) => product.category === categoryId)
}

export function searchCatalog(term) {
  const normalized = term.trim().toLowerCase()

  if (!normalized) {
    return []
  }

  return products.filter((product) => {
    const englishName = product.name.en.toLowerCase()
    const greekName = product.name.el.toLowerCase()
    const category = product.category.toLowerCase()
    return englishName.includes(normalized) || greekName.includes(normalized) || category.includes(normalized)
  })
}
